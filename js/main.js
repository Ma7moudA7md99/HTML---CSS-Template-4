// toggle between sections
$("#dashboard-span").click(() => {
  $("#dashboard-section").css({ opacity: "1" }, { "z-index": "999" });
  $("#settings-section").css({ opacity: "0" }, { "z-index": "0" });
  $("#profile-section").css({ opacity: "0" }, { "z-index": "0" });
  $("#projects-section").css({ opacity: "0" }, { "z-index": "0" });
  $("#courses-section").css({ opacity: "0" }, { "z-index": "0" });
  $("#friends-section").css({ opacity: "0" }, { "z-index": "0" });
  $("#files-section").css({ opacity: "0" }, { "z-index": "0" });
  $("#plans-section").css({ opacity: "0" }, { "z-index": "0" });
  $("aside.main").css("left", "-100%");
  $("i.fa-bars").css("display", "block");
  $("i.fa-bars-staggered").css("display", "none");
});
